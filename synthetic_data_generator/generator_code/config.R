#### FORMATTING
getSQLattrNames <- function(attrNames,name_sep=NAME_SEP){
  
  attrNames = gsub("\\.",name_sep, attrNames)
  attrNames = gsub(" ",name_sep, attrNames)
  attrNames = gsub("-",name_sep, attrNames)
  attrNames = gsub("/",name_sep, attrNames)
  
  return(attrNames)
}

